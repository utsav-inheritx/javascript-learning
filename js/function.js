function mulTbale() {
    for (i = 1; i <= 10; i++) {
        console.log(`2 X ${i} = ${2 * i}`);
    }
}
mulTbale();


function mulTbale1(num) {
    for (i = 1; i <= 10; i++) {
        document.write(`${num} X ${i} = ${num * i}`);
        document.write("<br>");
    }
}
mulTbale1(5);
mulTbale1(10);


