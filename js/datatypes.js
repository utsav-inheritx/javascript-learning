// String
let a = "Hello";
let b = 'Hello';
let c = "Hello\"How are you?\"";
console.log(a);
console.log(b);
console.log(c);


// Number -- Accept Positive and Negative value
let d = 5;
let e = 3.14;
console.log(d);
console.log(e);


// Boolean -- True or False
let f = 2 > 3;
console.log(f);


// Array
let g = ["A", "B", "C"];
console.log(g);


// Object
let h = { name: "ABC", age: 25 };
console.log(h);


// typeof
let i = typeof "ABC";
console.log(i);
